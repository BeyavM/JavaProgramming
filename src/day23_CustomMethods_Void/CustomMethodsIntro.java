package day23_CustomMethods_Void;

public class CustomMethodsIntro {

    public static void main(String[] args) { //start of method

        System.out.println("Test Started");

        greeting(); //will print out the method created below
        greetingContinued(); //will print out the method created below

        System.out.println("Test Complete");

    }

    public static void greeting(){ //start of method

        System.out.println("Hello Cydeo!");
        System.out.println("How are you today?");

        //end of method
    }

    public static void greetingContinued(){ //start of method

        System.out.println("I hope you are doing well!");
        System.out.println("This is how methods are made");

        //end of method
    }




}
