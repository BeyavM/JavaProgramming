package day17_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {

        if(true){
            System.out.println("Wooden Spoon"); //executed 1 time
        }

        System.out.println("-------------------------------------------");

        while(true){ //while is a repeated if statement
            System.out.println("Wooden Spoon"); //executed infinite times
        }





    }

}
