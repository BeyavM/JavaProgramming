package day09_IfStatements;

public class MaximumNumber {

    public static void main(String[] args) {

        int a = 50,
            b = 25;

        if (a > b) { //if a is greater than b
            System.out.println(a + " is greater than " + b);
        }else { //if it isn't
            System.out.println(b + " is greater than " + a);
        }

    }

}
