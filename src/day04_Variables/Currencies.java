package day04_Variables;

public class Currencies {

    public static void main(String[] args) {

        int dollar = 1000;

        double lira = dollar * 9.53;
        double euro = dollar * 0.86;
        double peso = dollar * 20.86;
        double pound = dollar * 0.73;
        double yuan = dollar * 7;
        double cad = dollar * 1.24;
        double yen = dollar * 114.14;
        double dirham = dollar * 3.67;
        double bam = dollar * 1.69;
        double rand = dollar * 15.41;

        System.out.println("lira = " + lira);
        System.out.println("euro = " + euro);
        System.out.println("peso = " + peso);
        System.out.println("pound = " + pound);
        System.out.println("yuan = " + yuan);
        System.out.println("cad = " + cad);
        System.out.println("yen = " + yen);
        System.out.println("dirham = " + dirham);
        System.out.println("bam = " + bam);
        System.out.println("rand = " + rand);


    }


}
