package day19_LoopPractice;

public class LoopsWithoutCurlyBraces {


    public static void main(String[] args) {
//with curly braces (default, use this only)
        for (int i = 0; i < 5; i++) {
            System.out.print("Beyav ");
            System.out.println("Muhsen");
        }

        System.out.println("-------------------------");
//example of without curly braces (can only have one print statement)
        for (int i = 0; i < 5; i++)
            System.out.println("Beyav Muhsen");






    }

}
