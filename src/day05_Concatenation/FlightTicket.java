package day05_Concatenation;

public class FlightTicket {

    public static void main(String[] args) {

        String from = "Binghamton, New York",
                to = "San Diego, California",
                 ticketPrice = "Prices starting from $214.99";

        System.out.println("From:" + "\n" + from + "\n" + "To:" + "\n" + to + "\n" + ticketPrice);

    }

}
