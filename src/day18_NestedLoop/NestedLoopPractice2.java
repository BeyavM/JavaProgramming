package day18_NestedLoop;

public class NestedLoopPractice2 {

    public static void main(String[] args) {
//example on nested loop printing an object 10 times
        for (int j = 0; j < 10; j++) { //nested loop

            for (int i = 0; i < 10; i++) {
                System.out.print("*  "); //for loop statement I want repeated 10 times
            }

           System.out.println(); //empty line for space

        }







    }

}
